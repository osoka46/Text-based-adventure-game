public class Inventory {

    private Armors armor;
    private Weapons weapon;

    public Inventory() {



    }


    public Armors getArmor() {
        return this.armor;
    }

    public void setArmor(Armors armor) {
        this.armor = armor;
    }

    public Weapons getWeapon() {
        return this.weapon;
    }

    public void setWeapon(Weapons weapon) {
        this.weapon = weapon;
    }
}
